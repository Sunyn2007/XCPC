#include <bits/stdc++.h>
using namespace std;
const double PI = 3.141593;
int t;
int main() {
    ios::sync_with_stdio(false);
    cin.tie(0), cout.tie(0);
    cin >> t;
    if (t == 1) cout << "I love Luogu!";
    if (t == 2) cout << 6 << ' ' << 4;
    if (t == 3) cout << 3 << '\n' << 12 << '\n' << 2;
    if (t == 4) cout << fixed << setprecision(3) << 500.0 / 3.0;
    if (t == 5) cout << 15;
    if (t == 6) cout << sqrt(117.0);
    if (t == 7) cout << 110 << '\n' << 90 << '\n' << 0;
    if (t == 8) cout << 2 * PI * 5 << '\n' << PI * 5 * 5 << '\n' << 4.0 / 3.0 * PI * 5 * 5 * 5;
    if (t == 9) cout << 22;
    if (t == 10) cout << 9;
    if (t == 11) cout << 100.0 / 3.0;
    if (t == 12) cout << 13 << '\n' << 'R';
    if (t == 13) cout << floor(cbrt(4.0 / 3.0 * PI * 4 * 4 * 4 + 4.0 / 3.0 * PI * 10 * 10 * 10));
    if (t == 14) cout << 50;
    return 0;
}